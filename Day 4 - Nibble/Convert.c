#include <stdint.h>


uint8_t RawBytes[] = {0xac ,0xe8,0x0e,0x6f,0xcc,0xa1,0x77,0x65,0x58,0xba,0xbe,0x2c,
0x51, 0xd6, 0xb6, 0x57, 0x65, 0x8a, 0x8a, 
0xBc, 0xf9, 0x73, 0xD1, 0xBB, 0x5C, 
0x93, 0x8a, 0xc9, 0xDA, 0x25, 0x2F, 
0xD4, 0xC9, 0x73
};


//5571c2017
uint8_t Key[] = { 0x55,0x71,0xc2,0x01,0x75,0x57,0x1c,0x20,0x17};

int main(void) {
	
	for (int i=0;i<sizeof(RawBytes);i++) {
		//printf("%02X %02X %c\n", RawBytes[i] , Key[i % sizeof(Key)], RawBytes[i] - Key[i % sizeof(Key)]);

		uint8_t H = (RawBytes[i] & 0xF0) >> 4;
		uint8_t L = (RawBytes[i] & 0xF);

		uint8_t KH = (Key[i % sizeof(Key)] & 0xF0) >> 4;
		uint8_t KL = (Key[i % sizeof(Key)] & 0xF);	

		H = (H - KH) & 0xF;
		L = (L - KL) & 0xF;
		
		uint8_t D = (H << 4) | L;

		printf("%c", D);
	}
	printf("\n");

	return 0;
}
